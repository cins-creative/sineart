async function main() {
  console.log("Fetching all articles from Supabase...");
  const rows = await get(
    "/rest/v1/dt_ly_thuyet_nen_tang?select=slug,content,video,video_tham_khao_khac&order=id.asc"
  );

  const bySlug = Object.fromEntries(rows.map((r) => [r.slug, r]));

  let created = 0;
  for (const slug of SLUGS) {
    const row = bySlug[slug];
    const dir = path.join(ROOT, "src", "app", "kien-thuc-nen-tang", slug);
    fs.mkdirSync(dir, { recursive: true });

    const contentFile = path.join(dir, "_content.ts");
    // Skip if already has real content (not stub)
    if (fs.existsSync(contentFile)) {
      const existing = fs.readFileSync(contentFile, "utf8");
      if (!existing.includes("STUB")) {
        console.log(`  skip (already migrated): ${slug}`);
        continue;
      }
    }

    const content = row?.content ?? "";
    const video = row?.video ?? "";
    const videoRef = row?.video_tham_khao_khac ?? "";

    const ts = `// AUTO-GENERATED by scripts/migrate-content.mjs — edit trực tiếp để thay thế DB.
// Khi chuyển sang block components, xoá file này và import blocks trong page.tsx thay thế.

export const CONTENT_HTML = ${JSON.stringify(content)};

export const VIDEO_URL = ${JSON.stringify(video)};

export const VIDEO_REF_URL = ${JSON.stringify(videoRef)};
`;
    fs.writeFileSync(contentFile, ts, "utf8");
    console.log(`  ✓ ${slug} (${content.length} chars)`);
    created++;
  }

  console.log(`\nDone. Created/updated ${created} content files.`);
}

main().catch((e) => { console.error(e); process.exit(1); });
