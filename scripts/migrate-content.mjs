function get(path) {
  return new Promise((resolve, reject) => {
    const opts = {
      hostname: HOST,
      path,
      method: "GET",
      headers: { apikey: KEY, Authorization: "Bearer " + KEY },
    };
    const req = https.request(opts, (res) => {
      let d = "";
      res.on("data", (c) => (d += c));
      res.on("end", () => resolve(JSON.parse(d)));
    });
    req.on("error", reject);
    req.end();
  });
}

